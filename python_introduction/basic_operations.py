#picking numbers
number1 = 10
number2 = 5

#calculating the sum of the two variables
calcSum = number2 + number1
print("Addition of " + str(number2) + " and " + str(number1) + " is " + str(calcSum))

#calculating the differences between the two variables
calcMinus = number1 - number2
print("Subtraction of " + str(number2) + " and " + str(number1) + " is " + str(calcMinus))

#multiplication the division of the two variables
calcMultiple = number2 * number1
print("Multiplication of " + str(number2) + " and " + str(number1) + " is " + str(calcMultiple))